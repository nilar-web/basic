//  >, >=, <, <=, ==, ===, !=

let x = 12;
let y = 3;

console.log(x > y);
console.log(x >= y);

console.log(x < y);
console.log(x <= y);

console.log(x == y);
console.log(x === y);

console.log(x != y);

console.log(2 == "2"); //true [check the value]
console.log(2 === "2"); //false [check the type and value]


